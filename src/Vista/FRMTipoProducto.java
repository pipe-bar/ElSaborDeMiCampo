    private void TBLTipoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBLTipoMouseClicked
      if (evt.getClickCount()==2) {
            int fila = TBLTipo.rowAtPoint(evt.getPoint());
            if (fila>-1) {
                TXTIdTipo.setValue((Integer)TBLTipo.getValueAt(fila, 0));
                TXTDescripcionTipo.setText((String)TBLTipo.getValueAt(fila, 1));
                BTNEliminar.setEnabled(true);
                BTNModificar.setEnabled(true);
                
            }
            
        }
    }//GEN-LAST:event_TBLTipoMouseClicked

