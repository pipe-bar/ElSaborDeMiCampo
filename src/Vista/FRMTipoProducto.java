    private void limpiarFormulario()
    {
    TXTIdTipo.setValue(0);
    TXTDescripcionTipo.setText("");
    BTNModificar.setEnabled(false);
    BTNEliminar.setEnabled(false);
    llenarTabla();
    }
    //LLENAR TABLA
    public void llenarTabla()
    {
    Tipo unTipo = new Tipo();
    Iterator<Tipo> itTipo = unTipo.listar();
    DefaultTableModel tabla = (DefaultTableModel)TBLTipo.getModel();
    Object[] filaTipo = new Object[TBLTipo.getColumnCount()];
    tabla.setRowCount(0);
        while (itTipo.hasNext()) {            
            unTipo = itTipo.next();
            filaTipo[0] = unTipo.getIdTipo();
            filaTipo[1] = unTipo.getDescripcionTipo();
            tabla.addRow(filaTipo);
        }
    
    }
    
    //LLENAR TABLA CON BUSQUEDA
    
    private void llenarTablaConBusqueda(String busqueda)
    {
    Tipo unTipo = new Tipo();
    Iterator<Tipo> itTipo = unTipo.buscar(busqueda);
    DefaultTableModel tabla = (DefaultTableModel)TBLTipo.getModel();
    Object[] filaTipo = new Object[TBLTipo.getColumnCount()];
    tabla.setRowCount(0);
        while (itTipo.hasNext()) {            
            unTipo = itTipo.next();
            filaTipo[0] = unTipo.getIdTipo();
            filaTipo[1] = unTipo.getDescripcionTipo();
            ((DefaultTableModel)TBLTipo.getModel()).addRow(filaTipo);
        }
    }
    
    private Tipo obtenerTipo()
    {
    Tipo elTipo = new Tipo();
    elTipo.setIdTipo((Integer)TXTIdTipo.getValue());
    elTipo.setDescripcionTipo(TXTDescripcionTipo.getText());
    return elTipo;
    }
