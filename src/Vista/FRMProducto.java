        //LLENAR EL COMBO PROVEEDOR
       private void llenarComboProveedor()
       {
      Proveedor unProveedor = new Proveedor();
      Iterator<Proveedor> itProveedor = unProveedor.listar();
      CBOProveedorProducto.removeAllItems();
      Proveedor seleccionar = new Proveedor();
      seleccionar.setNombreProveedor("seleccione Proveedor");
      CBOProveedorProducto.addItem(seleccionar);
           while (itProveedor.hasNext()) {
              CBOProveedorProducto.addItem(itProveedor.next());
           }
       }
       
