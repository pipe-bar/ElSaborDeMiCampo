    private void BTNReporteProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNReporteProductoActionPerformed
       JasperReport jrReporte = null;
        try {
            jrReporte = (JasperReport)JRLoader.loadObject(getClass().getClassLoader().getResource("Reportes/ReportesProyecto.jasper"));
            Map params = new HashMap();
            params.put("TipoDeProducto", "Seco");
            JasperPrint jpReporte = JasperFillManager.fillReport(jrReporte, params,ConexionDB.conexion);
            JasperViewer jReporteViewer = new JasperViewer(jpReporte,false);
            jReporteViewer.setVisible(true);
            System.out.println("se ha creado un reporte");
        } catch (JRException e) {
            System.out.println("error en el Jasper que no vas a poder solucionar"+e.getMessage());
        }
    }//GEN-LAST:event_BTNReporteProductoActionPerformed


