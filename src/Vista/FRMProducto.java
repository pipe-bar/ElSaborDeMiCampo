    private void TBLProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBLProductoMouseClicked
        if (evt.getClickCount()==2) {
            int fila = TBLProducto.rowAtPoint(evt.getPoint());
            if (fila>-1) {
                TXTIdProducto.setValue(   (Integer)TBLProducto.getValueAt(fila, 0));
                TXTNombreProducto.setText((String)TBLProducto.getValueAt(fila, 1));
                TXTPesoProducto.setValue((Integer)TBLProducto.getValueAt(fila, 2));
                CBOProveedorProducto.setSelectedItem((Proveedor)TBLProducto.getValueAt(fila, 3));
                CBOTipoProducto.setSelectedItem((Tipo)TBLProducto.getValueAt(fila, 4));
                TXTPrecioProducto.setValue((Integer)TBLProducto.getValueAt(fila, 5));
                 BTNEliminar.setEnabled(true);
                BTNModificar.setEnabled(true);
            }
        }
        
        
    }//GEN-LAST:event_TBLProductoMouseClicked

