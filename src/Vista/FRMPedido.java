//LLENAR TABLA CON BUSQUEDA

private void llenarTablaConBusqueda(String busqueda)
{
Pedido unPedido = new Pedido();
     DefaultTableModel tabla = ( DefaultTableModel)TBLPedido.getModel();
     Iterator<Pedido> itPedido = unPedido.buscar(busqueda);
     Object[] filaPedido = new Object[TBLPedido.getColumnCount()];
     tabla.setRowCount(0);
     while (itPedido.hasNext()) {        
        unPedido = itPedido.next();
        filaPedido[0]= unPedido.getIdPedido();
        filaPedido[1] = new Proveedor().buscarPorId(unPedido.getIdProveedorPedido());
        filaPedido[2] = new Producto().buscarPorId(unPedido.getIdProductoPedido());
        filaPedido[3] = unPedido.getObservacionPedido();
        filaPedido[4] = unPedido.getFechaPedido();
       
        ((DefaultTableModel)TBLPedido.getModel()).addRow(filaPedido);
}
}
