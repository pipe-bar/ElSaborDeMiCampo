    private void TBLPedidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBLPedidoMouseClicked
       if (evt.getClickCount()==1) {
            int fila = TBLPedido.rowAtPoint(evt.getPoint());
            if (fila>-1) {
                TXTIdPedido.setValue((Integer)  TBLPedido.getValueAt(fila, 0));
                CBOProveedor.setSelectedItem((Proveedor)TBLPedido.getValueAt(fila, 1));
                CBOProducto.setSelectedItem((Producto)TBLPedido.getValueAt(fila, 2));
                TXTObservacionPedido.setText((String)TBLPedido.getValueAt(fila, 3));
                BTNEliminar.setEnabled(true);
                BTNModificar.setEnabled(true);
             
            }
        }
    }//GEN-LAST:event_TBLPedidoMouseClicked

